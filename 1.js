
$(document).ready(function(){
    $(".move").mouseover(function(){
        $(".move").animate({left: '2px'} , "fast");
		$(".move").stop();
    });
   $(".move").mouseout(function(){
        $(".move").animate({left: '0px'} , "fast");
    });
});

